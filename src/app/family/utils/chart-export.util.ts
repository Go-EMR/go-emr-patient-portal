/**
 * Serializes the given SVGElement to a Blob and triggers a browser download.
 */
export function exportSVG(svgElement: SVGElement, filename: string): void {
  const serializer = new XMLSerializer();
  const svgStr = serializer.serializeToString(svgElement);
  const blob = new Blob(
    [`<?xml version="1.0" encoding="utf-8"?>\n${svgStr}`],
    { type: 'image/svg+xml;charset=utf-8' }
  );
  triggerDownload(blob, ensureExtension(filename, '.svg'));
}

