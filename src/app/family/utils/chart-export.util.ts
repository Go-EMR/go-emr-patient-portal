/**
 * Generates a CanRisk-compatible pedigree text format.
 * CanRisk is used by the Manchester CanRisk tool for BRCA/BOADICEA risk modelling.
 *
 * Returns the formatted string — caller decides how to present/download it.
 *
 * Reference: https://canrisk.org/canrisk_file_format/
 */
export function exportCanRisk(members: FamilyMember[]): string {
  const lines: string[] = [];

  lines.push('##CanRisk 2.0');
  lines.push('##HEADER');
  lines.push('##FamID\tName\tTarget\tIndivID\tFathID\tMothID\tSex\tMZtwin\tDead\tAge\tYob\tBC1\tBC2\tOC\tPRO\tPAN\tAshkn\tBRCA1\tBRCA2\tPALB2\tATM\tCHEK2\tBARD1\tRAD51D\tRAD51C\tBRIP1\tER:PR:HER2:CK14:CK56');
  lines.push('##DATA');

  const proband = members.find(m => m.isProband);
  const probandId = proband?.id ?? 'fm-001';

  // Map member IDs to simple integer IDs for CanRisk
  const idMap = new Map<string, number>();
  members.forEach((m, i) => idMap.set(m.id, i + 1));

  for (const member of members) {
    if (member.isPet) continue;

    const famId   = '1';
    const name    = `${member.firstName.replace(/\s/g, '_')}_${member.lastName.replace(/\s/g, '_')}`;
    const target  = member.isProband ? '1' : '0';
    const indivId = String(idMap.get(member.id) ?? 0);
    const fathId  = '0'; // Simplified — full pedigree linkage requires parent IDs
    const mothId  = '0';
    const sex     = member.sexAtBirth === 'male' ? 'M' : (member.sexAtBirth === 'female' ? 'F' : 'U');
    const mzTwin  = '0';
    const dead    = member.isDeceased ? '1' : '0';

    const currentYear = new Date().getFullYear();
    const yob = member.dateOfBirth ? member.dateOfBirth.getFullYear() : 0;
    const age = yob > 0
      ? (member.isDeceased ? (member.deceasedAge ?? currentYear - yob) : currentYear - yob)
      : 0;

    // Cancer flags (0 = unaffected, age at diagnosis = affected)
    const bc1 = getCancerAge(member, 'Breast Cancer', 'affected') ?? '0';
    const bc2 = '0'; // Contralateral BC
    const oc  = getCancerAge(member, 'Ovarian Cancer', 'affected') ?? '0';
    const pro = getCancerAge(member, 'Prostate Cancer', 'affected') ?? '0';
    const pan = getCancerAge(member, 'Pancreatic Cancer', 'affected') ?? '0';

    // Ashkenazi Jewish flag — not tracked, default 0
    const ashkn = '0';

    // Genetic test results (0 = not tested, P = pathogenic, N = negative, S = VUS)
    const brca1  = getGeneResult(member, 'BRCA1');
    const brca2  = getGeneResult(member, 'BRCA2');
    const palb2  = getGeneResult(member, 'PALB2');
    const atm    = getGeneResult(member, 'ATM');
    const chek2  = getGeneResult(member, 'CHEK2');
    const bard1  = getGeneResult(member, 'BARD1');
    const rad51d = getGeneResult(member, 'RAD51D');
    const rad51c = getGeneResult(member, 'RAD51C');
    const brip1  = getGeneResult(member, 'BRIP1');

    // ER:PR:HER2:CK14:CK56 tumour markers — not tracked
    const tumour = '0:0:0:0:0';

    const row = [
      famId, name, target, indivId, fathId, mothId, sex, mzTwin, dead,
      String(age), String(yob),
      String(bc1), String(bc2), String(oc), String(pro), String(pan),
      ashkn,
      brca1, brca2, palb2, atm, chek2, bard1, rad51d, rad51c, brip1,
      tumour,
    ].join('\t');

    lines.push(row);
  }

  return lines.join('\n');
}

