/**
 * Opens a new browser window with a print-ready HTML pedigree summary.
 */
export function exportPrintableHTML(
  members: FamilyMember[],
  conditions: FamilyCondition[]
): void {
  const now = new Date().toLocaleDateString('en-US', {
    year: 'numeric', month: 'long', day: 'numeric',
  });

  const memberRows = members
    .filter(m => !m.isPet)
    .map(m => {
      const memberConditions = conditions
        .filter(c => c.memberId === m.id)
        .map(c => `${c.conditionName} (${c.status})`)
        .join('; ') || '—';

      const dob = m.dateOfBirth
        ? m.dateOfBirth.toLocaleDateString('en-US')
        : '—';

      return `
        <tr>
          <td>${m.firstName} ${m.lastName}</td>
          <td>${m.relationship}</td>
          <td>${m.sexAtBirth}</td>
          <td>${dob}</td>
          <td>${m.isDeceased ? `Deceased (age ${m.deceasedAge ?? '?'})` : 'Living'}</td>
          <td>${memberConditions}</td>
        </tr>
      `;
    })
    .join('');

  const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="utf-8" />
  <title>Family Pedigree Report</title>
  <style>
    body {
      font-family: 'Times New Roman', serif;
      margin: 40px;
      color: #111;
    }
    h1 { font-size: 20px; margin-bottom: 4px; }
    .subtitle { font-size: 12px; color: #555; margin-bottom: 24px; }
    table {
      width: 100%;
      border-collapse: collapse;
      font-size: 12px;
    }
    th {
      background: #1f2937;
      color: #fff;
      padding: 8px 10px;
      text-align: left;
    }
    td {
      border: 1px solid #d1d5db;
      padding: 6px 10px;
    }
    tr:nth-child(even) td { background: #f9fafb; }
    .footer {
      margin-top: 32px;
      font-size: 10px;
      color: #6b7280;
      border-top: 1px solid #e5e7eb;
      padding-top: 12px;
    }
    @media print {
      body { margin: 20px; }
      .no-print { display: none; }
    }
  </style>
</head>
<body>
  <h1>Family Pedigree Report</h1>
  <div class="subtitle">Generated on ${now} — HIPAA-compliant clinical summary</div>

  <table>
    <thead>
      <tr>
        <th>Name</th>
        <th>Relationship</th>
        <th>Sex at Birth</th>
        <th>Date of Birth</th>
        <th>Status</th>
        <th>Conditions</th>
      </tr>
    </thead>
    <tbody>
      ${memberRows}
    </tbody>
  </table>

  <div class="footer">
    This report was generated by the GoHealth Patient Portal Family History module.
    For clinical use only. Handle in accordance with HIPAA regulations.
  </div>

  <script>
    window.onload = function() { window.print(); };
  </script>
</body>
</html>`;

  const win = window.open('', '_blank');
  if (win) {
    win.document.write(html);
    win.document.close();
  }
}

