/**
 * Generates a PED format string compatible with PLINK and similar tools.
 *
 * PED format columns:
 *   Family ID | Individual ID | Father ID | Mother ID | Sex | Phenotype
 *
 * Returns the formatted string.
 */
export function exportPED(members: FamilyMember[]): string {
  const lines: string[] = [];
  lines.push('# PED format generated by GoHealth Patient Portal');
  lines.push('# FamilyID IndividualID FatherID MotherID Sex(1=M,2=F,other=0) Phenotype(1=ctrl,2=case,-9=missing)');

  const famId = '1';
  const idMap = new Map<string, number>();
  members.forEach((m, i) => idMap.set(m.id, i + 1));

  for (const member of members) {
    if (member.isPet) continue;

    const indivId = String(idMap.get(member.id) ?? 0);
    const fathId  = '0';
    const mothId  = '0';
    const sex     = member.sexAtBirth === 'male' ? '1' : (member.sexAtBirth === 'female' ? '2' : '0');

    // Phenotype: 2 = affected (any condition), 1 = unaffected, -9 = unknown
    let phenotype = '1';
    if (member.conditions.some(c => c.status === 'affected')) {
      phenotype = '2';
    } else if (member.conditions.some(c => c.status === 'unknown')) {
      phenotype = '-9';
    }

    lines.push([famId, indivId, fathId, mothId, sex, phenotype].join('\t'));
  }

  return lines.join('\n');
}

// =============================================================================
// Private helpers
// =============================================================================

function triggerDownload(blob: Blob, filename: string): void {
  const url = URL.createObjectURL(blob);
  const a   = document.createElement('a');
  a.href     = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 60_000);
}

function ensureExtension(filename: string, ext: string): string {
  return filename.endsWith(ext) ? filename : `${filename}${ext}`;
}

function getCancerAge(
  member: FamilyMember,
  conditionName: string,
  status: string
): number | null {
  const condition = member.conditions.find(
    c => c.conditionName.toLowerCase().includes(conditionName.toLowerCase()) &&
         c.status === status
  );
  return condition?.onsetAge ?? null;
}

function getGeneResult(member: FamilyMember, geneName: string): string {
  const test = member.geneticTests.find(
    t => t.geneName.toUpperCase() === geneName.toUpperCase()
  );
  if (!test) return '0';
  switch (test.classification) {
    case 'pathogenic':
    case 'likely-pathogenic': return 'P';
    case 'benign':
    case 'likely-benign':    return 'N';
    case 'vus':               return 'S';
    default:                  return '0';
  }
}
