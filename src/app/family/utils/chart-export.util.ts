/**
 * Renders the SVG to a canvas and exports as PNG.
 * Uses the browser's Image and Canvas APIs — no external dependency required.
 */
export function exportPNG(svgElement: SVGElement, filename: string): void {
  const serializer = new XMLSerializer();
  const svgStr = serializer.serializeToString(svgElement);
  const svgBlob = new Blob([svgStr], { type: 'image/svg+xml;charset=utf-8' });
  const url = URL.createObjectURL(svgBlob);

  const img = new Image();
  img.onload = () => {
    const canvas = document.createElement('canvas');
    const scale  = window.devicePixelRatio || 2;
    canvas.width  = img.width  * scale;
    canvas.height = img.height * scale;

    const ctx = canvas.getContext('2d');
    if (!ctx) {
      URL.revokeObjectURL(url);
      return;
    }

    ctx.scale(scale, scale);
    ctx.fillStyle = '#ffffff';
    ctx.fillRect(0, 0, canvas.width, canvas.height);
    ctx.drawImage(img, 0, 0);
    URL.revokeObjectURL(url);

    canvas.toBlob(blob => {
      if (blob) triggerDownload(blob, ensureExtension(filename, '.png'));
    }, 'image/png');
  };

  img.onerror = () => URL.revokeObjectURL(url);
  img.src = url;
}

